IcoSphere::IcoSphere(const size_t& subdivisions)
{
    _subdivisions = subdivisions;

    float* vertices;
    size_t* triangles;

    size_t vertexCount;
    size_t triangleCount;

    if (_subdivisions == 1)
    {
        vertices = VERTICES_ICOSPHERE_1;
        vertexCount = VERTEX_COUNT_ICOSPHERE_1;

        triangles = TRIANGLES_ICOSPHERE_1;
        triangleCount = TRIANGLE_COUNT_ICOSPHERE_1;
    }
    else if (_subdivisions == 2)
    {
        vertices = VERTICES_ICOSPHERE_2;
        vertexCount = VERTEX_COUNT_ICOSPHERE_2;

        triangles = TRIANGLES_ICOSPHERE_2;
        triangleCount = TRIANGLE_COUNT_ICOSPHERE_2;
    }
    else
    {
        vertices = VERTICES_ICOSPHERE_3;
        vertexCount = VERTEX_COUNT_ICOSPHERE_3;

        triangles = TRIANGLES_ICOSPHERE_3;
        triangleCount = TRIANGLE_COUNT_ICOSPHERE_3;
    }

    _vertices = new Vertex[vertexCount];
    _triangles = new Triangle[triangleCount];

    for (size_t i = 0; i < vertexCount; ++i)
    {
        _vertices[i] = Vertex(vertices[3 * i + 0], vertices[3 * i + 1], vertices[3 * i + 2]);
    }

    for (size_t i = 0; i < triangleCount;++i)
    {
        _triangles[i] = Triangle(triangles[3 * i], triangles[3 * i + 1], triangles[3 * i + 2]);
    }

    _numberVertices = vertexCount;
    _numberTriangles = triangleCount;
}

